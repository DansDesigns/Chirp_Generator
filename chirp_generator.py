class ScrollableFrame(tk.Frame):
    """Vertically scrollable container — place widgets in self.inner."""
    def __init__(self, parent, **kw):
        super().__init__(parent, **kw)
        self._cv = tk.Canvas(self, bg=DARK_BG, highlightthickness=0, borderwidth=0)
        vsb = ttk.Scrollbar(self, orient="vertical", command=self._cv.yview)
        self._cv.configure(yscrollcommand=vsb.set)
        vsb.pack(side="right", fill="y")
        self._cv.pack(side="left", fill="both", expand=True)
        self.inner = tk.Frame(self._cv, bg=DARK_BG)
        self._win  = self._cv.create_window((0, 0), window=self.inner, anchor="nw")
        self.inner.bind("<Configure>", lambda e:
            self._cv.configure(scrollregion=self._cv.bbox("all")))
        self._cv.bind("<Configure>", lambda e:
            self._cv.itemconfig(self._win, width=e.width))
        for seq in ("<MouseWheel>", "<Button-4>", "<Button-5>"):
            self._cv.bind_all(seq, self._wheel)

    def _wheel(self, e):
        if   e.num == 4: self._cv.yview_scroll(-1, "units")
        elif e.num == 5: self._cv.yview_scroll( 1, "units")
        else:            self._cv.yview_scroll(int(-e.delta / 120), "units")


