# ═══════════════════════════════════════════════════════════════════════════════
#  Synthesis
# ═══════════════════════════════════════════════════════════════════════════════

def make_envelope(n, attack, release):
    env = np.ones(n)
    atk = min(int(attack * SAMPLE_RATE), max(1, int(n * 0.3)))
    rel = min(int(release * SAMPLE_RATE), max(1, int(n * 0.6)))
    if atk > 0: env[:atk]     = np.linspace(0, 1, atk)
    if rel > 0: env[n - rel:] = np.linspace(1, 0, rel)
    return env


def synth_segment(n, start_freq, end_freq, fm_amount, fm_ratio,
                  brightness, attack, release, volume):
    t       = np.arange(n) / SAMPLE_RATE
    mod     = fm_amount * np.sin(2 * np.pi * start_freq * fm_ratio * t)
    sweep   = np.linspace(start_freq, end_freq, n)
    inst    = np.maximum(sweep + mod, 20)
    phase   = 2 * np.pi * np.cumsum(inst) / SAMPLE_RATE
    carrier = np.sin(phase)
    if brightness > 0.05:
        p2      = 2 * np.pi * np.cumsum(inst * 2) / SAMPLE_RATE
        carrier = carrier * (1 - brightness * 0.4) + np.sin(p2) * brightness * 0.4
    return carrier * make_envelope(n, attack, release) * volume


def build_default_segments(style, duration):
    if style == "double":
        return [(0.0, duration * 0.55), (duration * 0.65, duration * 0.35)]
    elif style == "trill":
        gap = duration * 0.05; sd = (duration - gap * 2) / 3
        return [(0.0, sd), (sd + gap, sd), (2 * sd + 2 * gap, sd)]
    else:
        return [(0.0, duration)]


def n_blips_for_style(style):
    return {"double": 2, "trill": 3}.get(style, 1)


