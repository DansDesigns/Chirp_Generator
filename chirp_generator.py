    # ── style change ──────────────────────────────────────────────────────────

    def _on_style_change(self):
        style = next(k for l, k in STYLES if l == self.style_var.get())
        self.freq_panel.rebuild(n_blips_for_style(style), preserve=True)
        self._generate(reset_positions=True)

    # ── presets ───────────────────────────────────────────────────────────────

    def _apply_preset(self, p):
        lbl = next(l for l, k in STYLES if k == p["style"])
        self.style_var.set(lbl)
        self.freq_panel.rebuild(n_blips_for_style(p["style"]), preserve=False)
        self.freq_panel.set_freqs(p["freqs"])
        self.s_dur.set(p["dur"]); self.s_fma.set(p["fma"])
        self.s_fmr.set(p["fmr"]); self.s_br.set(p["br"])
        self.s_atk.set(p["atk"]); self.s_rel.set(p["rel"])
        self.s_vol.set(p["vol"])
        self._generate(reset_positions=True)

    # ── generate ──────────────────────────────────────────────────────────────

    def _generate(self, reset_positions=False):
        style    = next(k for l, k in STYLES if l == self.style_var.get())
        duration = self.s_dur.get()
        fm_amt   = self.s_fma.get()
        fm_ratio = self.s_fmr.get()
        bright   = self.s_br.get()
        attack   = self.s_atk.get()
        release  = self.s_rel.get()
        volume   = self.s_vol.get()

        default_segs = build_default_segments(style, duration)
        n_segs       = len(default_segs)
        freqs        = self.freq_panel.get_freqs()

        existing = self.wave_canvas.get_start_times()
        starts   = ([s for s, _ in default_segs]
                    if reset_positions or len(existing) != n_segs
                    else existing)

        audio_segs = []
        for i, (_, dur_s) in enumerate(default_segs):
            n      = int(dur_s * SAMPLE_RATE)
            sf, ef = freqs[i] if i < len(freqs) else (900, 1400)
            audio_segs.append(
                synth_segment(n, sf, ef, fm_amt, fm_ratio, bright, attack, release, volume)
            )

        self.wave_canvas.set_segments(
            list(zip(starts, [d for _, d in default_segs])), audio_segs)
        self._current_audio = self.wave_canvas.get_mixed_audio()
        dur_ms = int(len(self._current_audio) / SAMPLE_RATE * 1000)
        self.status_var.set(
            f"Generated — {n_segs} blip{'s' if n_segs>1 else ''}  ·  "
            f"{dur_ms} ms  ·  {SAMPLE_RATE} Hz  ·  16-bit mono"
        )

    def _on_timeline_drag(self):
        self._current_audio = self.wave_canvas.get_mixed_audio()
        starts = [f"{s*1000:.0f}" for s in self.wave_canvas.get_start_times()]
        dur_ms = int(len(self._current_audio) / SAMPLE_RATE * 1000)
        self.status_var.set(f"Positions: {', '.join(starts)} ms  ·  {dur_ms} ms total")

    # ── transport ─────────────────────────────────────────────────────────────

    def _play(self):
        if self._current_audio is None: self._generate()
        audio = self._current_audio.copy()
        def _do():
            try:    play_audio(audio)
            except Exception as e: self.status_var.set(f"Playback error: {e}")
        threading.Thread(target=_do, daemon=True).start()

    # ── export ────────────────────────────────────────────────────────────────

    def _save_wav(self):
        if self._current_audio is None: self._generate()
        path = filedialog.asksaveasfilename(
            defaultextension=".wav", filetypes=[("WAV audio", "*.wav")],
            initialfile="comm_chirp.wav")
        if not path: return
        try:
            save_wav(path, self._current_audio)
            self.status_var.set(f"Saved WAV → {os.path.basename(path)}")
        except Exception as e:
            messagebox.showerror("Save error", str(e))

    def _save_mp3(self):
        if self._current_audio is None: self._generate()
        path = filedialog.asksaveasfilename(
            defaultextension=".mp3", filetypes=[("MP3 audio", "*.mp3")],
            initialfile="comm_chirp.mp3")
        if not path: return
        try:
            save_mp3(path, self._current_audio)
            self.status_var.set(f"Saved MP3 → {os.path.basename(path)}")
        except Exception as e:
            messagebox.showerror("Save error", f"{e}\n\nMake sure ffmpeg is on your PATH.")

    def _save_arduino_h(self):
        if self._current_audio is None: self._generate()
        path = filedialog.asksaveasfilename(
            defaultextension=".h", filetypes=[("C header", "*.h")],
            initialfile="comm_chirp.h")
        if not path: return
        try:
            # Use the filename stem as the C variable name, sanitised
            stem = os.path.splitext(os.path.basename(path))[0]
            var  = "".join(c if c.isalnum() else "_" for c in stem).lstrip("0123456789") or "chirp"
            save_arduino_h(path, self._current_audio, var_name=var)
            n = len(self._current_audio)
            self.status_var.set(
                f"Saved Arduino .h → {os.path.basename(path)}  "
                f"({n} samples, 8-bit unsigned PCM)"
            )
        except Exception as e:
            messagebox.showerror("Save error", str(e))


