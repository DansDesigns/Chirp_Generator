def audio_to_int16(a):
    return (np.clip(a, -1, 1) * 32767).astype(np.int16)

def save_wav(path, audio):
    with wave.open(path, "w") as wf:
        wf.setnchannels(1); wf.setsampwidth(2); wf.setframerate(SAMPLE_RATE)
        wf.writeframes(audio_to_int16(audio).tobytes())

def save_mp3(path, audio):
    if not HAS_PYDUB:
        raise RuntimeError("pydub not installed — run: pip install pydub")
    if not HAS_FFMPEG:
        raise RuntimeError(
            "ffmpeg not found on PATH.\n"
            "Install it and try again:\n"
            "  Ubuntu/Debian:  sudo apt install ffmpeg\n"
            "  Fedora:         sudo dnf install ffmpeg\n"
            "  Arch:           sudo pacman -S ffmpeg\n"
            "  Windows:        https://ffmpeg.org/download.html"
        )
    tmp = tempfile.mktemp(suffix=".wav")
    save_wav(tmp, audio)
    try:
        AudioSegment.from_wav(tmp).export(path, format="mp3", bitrate="192k")
    finally:
        if os.path.exists(tmp):
            os.remove(tmp)

def save_arduino_h(path, audio, sample_rate=SAMPLE_RATE, var_name="chirp"):
    """
    Export audio as a C header file for use with Arduino / ESP32.

    Usage in your sketch:
        #include "chirp.h"
        // play with ESP32 DAC, I2S, or PWM using CHIRP_DATA / CHIRP_SAMPLES
    """
    # Resample to 8-bit unsigned (0-255) — DAC-friendly for ESP32
    normalised = np.clip(audio, -1.0, 1.0)
    samples_u8 = ((normalised + 1.0) * 127.5).astype(np.uint8)
    n = len(samples_u8)

    guard = var_name.upper() + "_H"
    lines = [
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        "// Auto-generated by Chirp Generator",
        f"// {n} samples @ {sample_rate} Hz  ({n/sample_rate*1000:.1f} ms)",
        "// Format: 8-bit unsigned PCM (0-255, centre = 128)",
        "// ESP32 example:",
        "//   dacWrite(DAC1, pgm_read_byte(&CHIRP_DATA[i]));",
        "",
        f"#define CHIRP_SAMPLE_RATE {sample_rate}",
        f"#define CHIRP_SAMPLES      {n}",
        "",
        f"const uint8_t {var_name.upper()}_DATA[] PROGMEM = {{",
    ]

    # Write 16 bytes per line for readability
    for i in range(0, n, 16):
        chunk = samples_u8[i:i+16]
        row   = ", ".join(f"0x{b:02X}" for b in chunk)
        comma = "," if i + 16 < n else ""
        lines.append(f"  {row}{comma}")

    lines += ["};", "", f"#endif // {guard}", ""]

    with open(path, "w") as f:
        f.write("\n".join(lines))


def play_audio(audio):
    """Write audio to a temp WAV and play it via the system player."""
    import subprocess, sys
    tmp = tempfile.mktemp(suffix=".wav")
    try:
        save_wav(tmp, audio)
        if sys.platform == "darwin":
            subprocess.run(["afplay", tmp], check=False)
        elif sys.platform.startswith("linux"):
            # aplay: blocking call — wait for it to finish before deleting tmp
            if subprocess.run(["aplay", "-q", tmp], check=False).returncode != 0:
                # fallback: try paplay (PulseAudio) then ffplay
                if subprocess.run(["paplay", tmp], check=False).returncode != 0:
                    subprocess.run(["ffplay", "-nodisp", "-autoexit", "-loglevel", "quiet", tmp], check=False)
        elif sys.platform == "win32":
            import winsound
            winsound.PlaySound(tmp, winsound.SND_FILENAME)
    finally:
        try:
            os.remove(tmp)
        except OSError:
            pass


