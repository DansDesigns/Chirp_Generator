class Slider(tk.Canvas):
    H = 26; TRACK_H = 4; R = 9

    def __init__(self, parent, from_, to, value, resolution=1,
                 on_change=None, bg=None, **kw):
        _bg = bg or PANEL_BG
        super().__init__(parent, height=self.H, bg=_bg,
                         highlightthickness=0, cursor="hand2", **kw)
        self._from = from_; self._to = to; self._res = resolution
        self._val  = self._snap(float(value))
        self.on_change = on_change
        self._hover = self._dragging = False
        self.bind("<Configure>",       self._redraw)
        self.bind("<ButtonPress-1>",   self._press)
        self.bind("<B1-Motion>",       self._drag)
        self.bind("<ButtonRelease-1>", self._release)
        self.bind("<Enter>",           lambda e: self._seth(True))
        self.bind("<Leave>",           lambda e: self._seth(False))

    def _snap(self, v):
        v = max(self._from, min(self._to, v))
        return round(v / self._res) * self._res if self._res else v

    def _xfor(self, v):
        w = self.winfo_width() or 200; pad = self.R + 2
        return pad + (v - self._from) / max(self._to - self._from, 1e-9) * (w - 2*pad)

    def _vfor(self, x):
        w = self.winfo_width() or 200; pad = self.R + 2
        return self._snap(self._from + (x - pad) / max(w - 2*pad, 1) * (self._to - self._from))

    def _redraw(self, *_):
        self.delete("all")
        w = self.winfo_width() or 200; cy = self.H // 2; pad = self.R + 2
        hx = self._xfor(self._val)
        self.create_rectangle(pad, cy-self.TRACK_H//2, w-pad, cy+self.TRACK_H//2,
                               fill=TROUGH_BG, outline="")
        self.create_rectangle(pad, cy-self.TRACK_H//2, hx, cy+self.TRACK_H//2,
                               fill=ACCENT, outline="")
        col = HANDLE_ACT if (self._hover or self._dragging) else HANDLE_COL
        self.create_oval(hx-self.R, cy-self.R, hx+self.R, cy+self.R,
                         fill=col, outline="#8899cc", width=1)

    def _press(self, e):   self._dragging = True;  self._upd(e.x)
    def _drag(self, e):    self._upd(e.x)
    def _release(self, e): self._dragging = False; self._redraw()
    def _seth(self, v):    self._hover = v;        self._redraw()

    def _upd(self, x):
        nv = self._vfor(x)
        if nv != self._val:
            self._val = nv; self._redraw()
            if self.on_change: self.on_change(nv)

    def get(self): return self._val
    def set(self, v):
        self._val = self._snap(float(v)); self._redraw()
        if self.on_change: self.on_change(self._val)


# ═══════════════════════════════════════════════════════════════════════════════
#  SliderRow — label + hint + slider + live value
# ═══════════════════════════════════════════════════════════════════════════════

class SliderRow(tk.Frame):
    """
    A slider with:
      • a bold label on the left
      • a small italic hint line below the label
      • the slider bar filling available space
      • a live numeric readout on the right
    """
    def __init__(self, parent, label, hint, from_, to, default,
                 resolution=1, unit="", fmt="{:.0f}",
                 on_change=None, label_fg=None, bg=None, **kw):
        _bg = bg or PANEL_BG
        super().__init__(parent, bg=_bg, **kw)
        self._fmt_s = fmt; self._unit = unit; self._cb = on_change
        fg = label_fg or DARK_FG

        # Left column: label + hint stacked — sized by content, not clipped
        left = tk.Frame(self, bg=_bg)
        left.pack(side="left", fill="y", padx=(0, 4))
        tk.Label(left, text=label, bg=_bg, fg=fg,
                 font=("Helvetica", 11), anchor="w").pack(fill="x")
        tk.Label(left, text=hint, bg=_bg, fg=HINT_FG,
                 font=("Helvetica", 9), anchor="w", wraplength=148,
                 justify="left").pack(fill="x", pady=(0, 2))

        # Right: live value readout
        self._lbl = tk.Label(self, text=self._fv(default), bg=_bg, fg=fg,
                             font=("Helvetica", 11, "bold"), width=9, anchor="e")
        self._lbl.pack(side="right")

        # Slider fills the middle
        self.slider = Slider(self, from_=from_, to=to, value=default,
                             resolution=resolution, on_change=self._chg, bg=_bg)
        self.slider.pack(side="left", fill="x", expand=True, padx=(6, 6))

    def _fv(self, v):   return self._fmt_s.format(v) + self._unit
    def _chg(self, v):
        self._lbl.config(text=self._fv(v))
        if self._cb: self._cb(v)
    def get(self):      return self.slider.get()
    def set(self, v):
        self.slider.set(v)
        self._lbl.config(text=self._fv(v))


# ═══════════════════════════════════════════════════════════════════════════════
#  BlipFreqPanel — per-blip start/end frequency controls
# ═══════════════════════════════════════════════════════════════════════════════

class BlipFreqPanel(tk.Frame):
    _DEFAULTS = [(900, 1400), (900, 1400), (900, 1400)]

    def __init__(self, parent, on_change=None, **kw):
        super().__init__(parent, bg=PANEL_BG, **kw)
        self._on_change = on_change
        self._rows      = []   # [(container, start_SliderRow, end_SliderRow)]

    def rebuild(self, n_blips, preserve=True):
        old = [(r[2].get(), r[3].get()) for r in self._rows]
        # destroy every widget we own (border frame + content frame)
        for r in self._rows:
            r[0].destroy()   # border strip
            r[1].destroy()   # content frame
        self._rows = []

        for i in range(n_blips):
            col  = MARKER_COLS[i % len(MARKER_COLS)]
            bg   = self._tint(col)
            name = f"Blip {i+1}" if n_blips > 1 else "Blip"

            border = tk.Frame(self, bg=col, height=2)
            border.pack(fill="x", padx=2, pady=(8 if i > 0 else 2, 0))

            frm = tk.Frame(self, bg=bg)
            frm.pack(fill="x")

            tk.Label(frm, text=name.upper(), bg=bg, fg=col,
                     font=("Helvetica", 9, "bold"), anchor="w"
                     ).pack(fill="x", padx=10, pady=(5, 0))

            sv, ev = (old[i] if preserve and i < len(old)
                      else self._DEFAULTS[i % len(self._DEFAULTS)])

            sr = SliderRow(frm, "Start freq",
                           "pitch the blip begins at",
                           200, 3000, sv, 10, " Hz", "{:.0f}",
                           on_change=lambda _: self._fire(), label_fg=col, bg=bg)
            sr.pack(fill="x", padx=8, pady=3)

            er = SliderRow(frm, "End freq",
                           "pitch the blip lands on",
                           200, 3000, ev, 10, " Hz", "{:.0f}",
                           on_change=lambda _: self._fire(), label_fg=col, bg=bg)
            er.pack(fill="x", padx=8, pady=(0, 7))

            # store border AND content frame so both get destroyed next rebuild
            self._rows.append((border, frm, sr, er))

    def _fire(self):
        if self._on_change: self._on_change()

    def get_freqs(self):
        return [(r[2].get(), r[3].get()) for r in self._rows]

    def set_freqs(self, freq_list):
        for i, (s, e) in enumerate(freq_list):
            if i < len(self._rows):
                self._rows[i][2].set(s); self._rows[i][3].set(e)

    @staticmethod
    def _tint(c):
        return (f"#{int(c[1:3],16)//8:02x}"
                f"{int(c[3:5],16)//8:02x}"
                f"{int(c[5:7],16)//8:02x}")


