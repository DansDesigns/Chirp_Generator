    # ── generate ──────────────────────────────────────────────────────────────

    def _generate(self, reset_positions=False):
        # If looping, stop — user can restart loop with updated audio
        was_looping = self._looping
        if was_looping:
            self._stop_loop()

        style    = next(k for l, k in STYLES if l == self.style_var.get())
        fm_amt   = self.s_fma.get()
        fm_ratio = self.s_fmr.get()
        bright   = self.s_br.get()

        blips  = self.blip_panel.get_blip_params()  # [(sf, ef, dur, atk, rel, vol), ...]
        n_segs = len(blips)

        # Default positions: spread blips across the longest duration
        max_dur = max(b[2] for b in blips) if blips else 0.18

        def default_starts():
            if n_segs == 1:
                return [0.0]
            gap  = max_dur * 0.2
            step = max_dur + gap
            return [i * step for i in range(n_segs)]

        existing = self.wave_canvas.get_start_times()
        starts   = (default_starts()
                    if reset_positions or len(existing) != n_segs
                    else existing)

        audio_segs = []
        for sf, ef, dur, atk, rel, vol in blips:
            n = int(dur * SAMPLE_RATE)
            audio_segs.append(
                synth_segment(n, sf, ef, fm_amt, fm_ratio, bright, atk, rel, vol)
            )

        self.wave_canvas.set_segments(
            list(zip(starts, [b[2] for b in blips])), audio_segs)
        self._current_audio = self.wave_canvas.get_mixed_audio()
        dur_ms = int(len(self._current_audio) / SAMPLE_RATE * 1000)
        self.status_var.set(
            f"Generated — {n_segs} blip{'s' if n_segs>1 else ''}  ·  "
            f"{dur_ms} ms  ·  {SAMPLE_RATE} Hz  ·  16-bit mono"
        )

    def _on_timeline_drag(self):
        self._current_audio = self.wave_canvas.get_mixed_audio()
        starts = [f"{s*1000:.0f}" for s in self.wave_canvas.get_start_times()]
        dur_ms = int(len(self._current_audio) / SAMPLE_RATE * 1000)
        self.status_var.set(f"Positions: {', '.join(starts)} ms  ·  {dur_ms} ms total")

    # ── transport ─────────────────────────────────────────────────────────────

    def _play(self):
        """Play once, stopping any active loop first."""
        self._stop_loop()
        if self._current_audio is None: self._generate()
        audio = self._current_audio.copy()
        def _do():
            try:    play_audio(audio)
            except Exception as e: self.status_var.set(f"Playback error: {e}")
        threading.Thread(target=_do, daemon=True).start()

    def _toggle_loop(self):
        """Toggle looping on/off."""
        if self._looping:
            self._stop_loop()
        else:
            self._start_loop()

    def _start_loop(self):
        if self._current_audio is None: self._generate()
        self._looping = True
        self.loop_btn.config(text="◼  Stop", bg="#f38ba8", fg=DARK_BG,
                             activebackground="#d06070")
        self.status_var.set("Looping — click Stop to end")

        def _loop_worker():
            while self._looping:
                try:
                    audio = self._current_audio
                    if audio is None: break
                    play_audio(audio.copy())
                except Exception as e:
                    self.after(0, lambda: self.status_var.set(f"Loop error: {e}"))
                    break
            # Reset button on exit (schedule on main thread)
            self.after(0, self._reset_loop_btn)

        self._loop_thread = threading.Thread(target=_loop_worker, daemon=True)
        self._loop_thread.start()

    def _stop_loop(self):
        self._looping = False
        self._reset_loop_btn()

    def _reset_loop_btn(self):
        self._looping = False
        self.loop_btn.config(text="⟲  Loop", bg=BTN_BG, fg=DARK_FG,
                             activebackground=BTN_ACTIVE)

